public class Calculadora {

  public static float adiciona(float a, float b) {
    return a + b;
  }

  public static float subtrai(float a, float b) {
    return a - b;
  }

  public static float multiplica(float a, float b) {
    return a * b;
  }

  public static float divide(float a, float b) {
    return a / b;
  }
}
